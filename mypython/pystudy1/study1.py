# 在创建字符串的时候 可以在字符串中指定占位符
# %s 表示任意的字符串
# %f 浮点数占位符
# %d 整数占位符
b = 'hello %s' % '孙悟空'
print(b)
c = 'hello %s 你好 %s' % ('卡卡西', '宇智波鼬')
print(c)

d = 'hello%.2f' % 123.456
print(d)
e = 'hello%d' % 123.45
print(e)

print('a=%s' % e)

