# 标识符
# 在python 中 所有可以自主命名的内容都属于标识符
# 比如： 变量名   函数名  类名
# 标识符必须遵循标识符的规范
#  1 标识符中国可以保护 字母 数字 _ 但是不能用数字开头
#  2 标识符不能使用python的关键字与保留字
#    也不建议使用python中的函数名作为标识符
#  3 命名规范:
#      在python中注意准信两个命名规范:
#      下划线命名法：所有字母小写，单词之间使用_分割 xxx_yyy_zzz
#       帕斯卡命名法:大驼峰命名法:首字母大写 其余字母小写 其余字母小写
#
